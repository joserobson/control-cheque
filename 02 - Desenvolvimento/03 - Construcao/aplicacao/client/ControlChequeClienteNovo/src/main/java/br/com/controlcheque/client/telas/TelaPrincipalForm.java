    private void gerarBackup() {
        WorkBackup work = new WorkBackup();
        work.setParent(this);
        work.execute();
    }

    private class WorkBackup extends SwingWorker<Object, Object> {

        private JDialogProcessando aguarde = null;//new JDialogProcessando(this, "Aguarde...", "Aguarde Processando...");
        private JFrame parent;

        public WorkBackup() {
            aguarde = new JDialogProcessando(parent, "Aguarde...", "Aguarde Processando...");
            aguarde.setVisible(true);
        }

        @Override
        protected Object doInBackground() throws Exception {
            try {
                new UtilServerImpl().gerarBackup();
                return true;
            } catch (ClientChequeException ex) {
                throw ex;
            }
        }

        @Override
        protected void done() {
            aguarde.setVisible(false);
            try {
                if (get() != null) {
                    JOptionPane.showMessageDialog(null, "BACKUP GERADO COM SUCESSO", "BACKUP", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "ERRO AO GERAR BACKUP: " + ex.getMessage(), "BACKUP", JOptionPane.ERROR_MESSAGE);
            }

        }

        public void setParent(JFrame parent) {
            this.parent = parent;
        }
    }
